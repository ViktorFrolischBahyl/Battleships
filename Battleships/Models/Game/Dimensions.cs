namespace Battleships.Models.Game;

/// <summary>
/// Model representing dimensions of the game grid.
/// </summary>
public class Dimensions
{
    /// <summary>
    /// Gets or sets the x.
    /// </summary>
    /// <value>
    /// The x.
    /// </value>
    public int X { get; set; }

    /// <summary>
    /// Gets or sets the y.
    /// </summary>
    /// <value>
    /// The y.
    /// </value>
    public int Y { get; set; }
}